package Day03;

public abstract class BangunDatar {
    private double garisHorizontal;
    private double garisVertikal;
    private double garisDiagonal;
    private double jariJari;
    public abstract double Luas();
    public abstract double Keliling();
    
    public double getGarisHorizontal() {
        return garisHorizontal;
    }
    public void setGarisHorizontal(double garisHorizontal) {
        this.garisHorizontal = garisHorizontal;
    }
    public double getGarisVertikal() {
        return garisVertikal;
    }
    public void setGarisVertikal(double garisVertikal) {
        this.garisVertikal = garisVertikal;
    }
    public double getGarisDiagonal() {
        return garisDiagonal;
    }
    public void setGarisDiagonal(double garisDiagonal) {
        this.garisDiagonal = garisDiagonal;
    }
    public double getJariJari() {
        return jariJari;
    }
    public void setJariJari(double jariJari) {
        this.jariJari = jariJari;
    }

    

}
